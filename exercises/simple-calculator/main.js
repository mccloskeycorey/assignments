function adding() {
    var valueNum1 = document.getElementById("num1").value;
    var valueNum2 = document.getElementById("num2").value;
    document.write(parseInt(valueNum1) + parseInt(valueNum2));

}

function subtracting() {
    var valueNum1 = document.getElementById("num1").value;
    var valueNum2 = document.getElementById("num2").value;
    document.write(valueNum1 - valueNum2)
}

function multiplying() {
    var valueNum1 = document.getElementById("num1").value;
    var valueNum2 = document.getElementById("num2").value;
    document.write(valueNum1 * valueNum2)
}
